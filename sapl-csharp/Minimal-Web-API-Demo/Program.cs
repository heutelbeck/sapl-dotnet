using SAPL.AspNetCore.Security.Filter.Minimal_Web_API;
using SAPL.AspNetCore.Security.Subscription;
using SAPL.PDP.Api;
using SAPL.PDP.Client;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddSingleton<IPolicyDecisionPoint, PolicyDecisionPoint>();
builder.Services.AddSingleton<IAuthorizationSubscriptionBuilderService, AuthorizationSubscriptionBuilderService>();
var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

var summaries = new[]
{
    "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
};

app.MapGet("/weatherforecastPreEnforce", () =>
    {
        var forecast = Enumerable.Range(1, 5).Select(index =>
                new WeatherForecast
                (
                    DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
                    Random.Shared.Next(-20, 55),
                    summaries[Random.Shared.Next(summaries.Length)]
                ))
            .ToArray();
        return forecast;
    })
    .WithName("GetWeatherForecastPre")
    .WithOpenApi().AddEndpointFilter(new PreEnforceEndPointFilter("SomeSubject", "Weather", "someResource", "SomeEnvironment"));

app.MapGet("/weatherforecastPostEnforce", () =>
{
    var forecast = Enumerable.Range(1, 5).Select(index =>
        new WeatherForecast
        (
            DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
            Random.Shared.Next(-20, 55),
            summaries[Random.Shared.Next(summaries.Length)]
        ))
        .ToArray();
    return forecast;
})
.WithName("GetWeatherForecastPost")
.WithOpenApi().AddEndpointFilter(new PostEnforceEndPointFilter("SomeSubject", "Weather", "someResource", "SomeEnvironment"));




app.Run();

internal record WeatherForecast(DateOnly Date, int TemperatureC, string? Summary)
{
    public int TemperatureF => 32 + (int)(TemperatureC / 0.5556);
}
